private void logar(){
    try{ 
         String email,senha;
       
       email = txtEmailFieldLogin.getText();
       senha = txtSenhaFieldLogin.getText();
       
        UsuarioDTO usuarioLogado = new UsuarioDTO();
         usuarioLogado.setEmail(email);
         usuarioLogado.setSenha(senha);
         
        LoginDAO usuarioDAO = new LoginDAO();
         ResultSet resUsuariDao = usuarioDAO.autenticacao(usuarioLogado);
                 
                 if (resUsuariDao.next()) {
                   PerfilView telaPerfil = new PerfilView();
                   EditarView telaEditar = new EditarView();
                   UsuarioDTO DadosDosUsuarios = CapturarDados(resUsuariDao);
                   telaPerfil.capturarDados(DadosDosUsuarios);
                   telaEditar.PreencherCamposEditar(DadosDosUsuarios);
                   telaPerfil.setVisible(true);
                    dispose();
                  
                 }else{
                    
                    JOptionPane.showMessageDialog(null, "usuario ou senha inv√°lidos");
                 }
    
     }catch (SQLException erro){
         JOptionPane.showMessageDialog(null, "FRMLOgin" + erro);
     }
            
}

private UsuarioDTO CapturarDados(ResultSet dadosDoUsuario ){
    UsuarioDTO usuarioAtual = new UsuarioDTO();
    try{
    usuarioAtual.setEmail(dadosDoUsuario.getString("email"));
    usuarioAtual.setNome(dadosDoUsuario.getString("nome"));
    usuarioAtual.setTelefone(dadosDoUsuario.getString("telefone"));
    usuarioAtual.setcPF(dadosDoUsuario.getString("cpf"));
    usuarioAtual.setSenha(dadosDoUsuario.getString("senha"));
    usuarioAtual.setIdUsuario(Integer.parseInt(dadosDoUsuario.getString("id_usuario")));
    }catch(SQLException erro){
        JOptionPane.showMessageDialog(null, "erro na busca de dados" + erro);
    }
    return usuarioAtual;
}
